    public void BuildMaze()
    {
        float offsetX = (currentMaze.width - 1) * cellSize / 2f;
        float offsetZ = (currentMaze.height - 1) * cellSize / 2f;

        for (int y = 0; y < currentMaze.height; y++)
        {
            for (int x = 0; x < currentMaze.width; x++)
            {
                if (currentMaze.layout[y][x] == 1)
                {
                    Vector3 pos = new Vector3(x * cellSize - offsetX, 0, y * cellSize - offsetZ);
                    Instantiate(wallPrefab, pos, Quaternion.identity, transform);
                }
            }
        }
    }


